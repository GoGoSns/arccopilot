const detectNavigatorLocale = (): Locale => {
  if (typeof navigator === 'undefined') return 'en'
  return navigator.language?.toLowerCase().startsWith('tr') ? 'tr' : 'en'
}

const normalizePreference = (value: unknown): LocalePreference | null => {
  return value === 'auto' || value === 'en' || value === 'tr' ? value : null
}

const resolveLocale = (preference: LocalePreference): Locale => {
  return preference === 'auto' ? detectNavigatorLocale() : preference
}

