export const STRINGS = {
  en: EN,
  tr: TR,
} as const

type StringMap = typeof STRINGS.en

let activePreference: LocalePreference = 'auto'
let activeLocale: Locale = detectNavigatorLocale()
let initialized = false
let initPromise: Promise<Locale> | null = null
let storageListenerAttached = false

function emitChange(): void {
  for (const listener of listeners) {
    listener()
  }
}

function attachStorageListener(): void {
  if (storageListenerAttached || typeof chrome === 'undefined' || !chrome.storage?.onChanged) return

  chrome.storage.onChanged.addListener((changes, areaName) => {
    if (areaName !== 'local') return
    const change = changes[LOCALE_STORAGE_KEY]
    if (!change) return

    const preference = normalizePreference(change.newValue)
    if (preference) {
      activePreference = preference
      activeLocale = resolveLocale(preference)
    } else {
      activePreference = 'auto'
      activeLocale = detectNavigatorLocale()
      if (change.newValue != null) {
        void chromeStorageRemove(LOCALE_STORAGE_KEY)
      }
    }

    emitChange()
  })

  storageListenerAttached = true
}

async function hydrateLocale(): Promise<Locale> {
  activeLocale = detectNavigatorLocale()
  activePreference = 'auto'

  try {
    const stored = await chromeStorageGet(LOCALE_STORAGE_KEY)
    const preference = normalizePreference(stored[LOCALE_STORAGE_KEY])
    if (preference) {
      activePreference = preference
      activeLocale = resolveLocale(preference)
    } else if (stored[LOCALE_STORAGE_KEY] != null) {
      await chromeStorageRemove(LOCALE_STORAGE_KEY)
    }
  } catch {
    activePreference = 'auto'
    activeLocale = detectNavigatorLocale()
  }

  initialized = true
  attachStorageListener()
  return activeLocale
}

export async function initI18n(): Promise<Locale> {
  if (initialized) return activeLocale
  if (!initPromise) {
    initPromise = hydrateLocale().finally(() => {
      initPromise = null
    })
  }
  return initPromise
}

export async function getLocale(): Promise<Locale> {
  return initI18n()
}

export function getLocaleSync(): Locale {
  return activeLocale
}

export function getLocalePreference(): LocalePreference {
  return activePreference
}

export function getLocalePromptLanguage(locale: Locale = activeLocale): 'Turkish' | 'English' {
  return locale === 'tr' ? 'Turkish' : 'English'
}

export function t(key: string): string {
  const strings = STRINGS[activeLocale] as Record<string, string> | undefined
  return strings?.[key] ?? STRINGS.en[key as keyof StringMap] ?? key
}

export function formatText(key: string, values: Record<string, string | number>): string {
  let text = t(key)
  for (const [placeholder, value] of Object.entries(values)) {
    text = text.split(`{${placeholder}}`).join(String(value))
  }
  return text
}

export async function setLocale(locale: LocalePreference): Promise<void> {
  activePreference = locale
  activeLocale = resolveLocale(locale)
  emitChange()
  await chromeStorageSet({ [LOCALE_STORAGE_KEY]: locale })
}

export function useLocale(): Locale {
  return useSyncExternalStore(
    (listener) => {
      listeners.add(listener)
      return () => {
        listeners.delete(listener)
      }
    },
    () => activeLocale,
    () => activeLocale,
  )
}
